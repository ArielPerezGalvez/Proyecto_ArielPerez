    public ArrayList<Integer> Top() {
        ArrayList<Integer> a = new ArrayList();
        for (int i = 0; i < Cont.size(); i++) {
            a.add(Cont.get(i));
        }
        Collections.sort(a, Collections.reverseOrder());
        ArrayList<Integer> arreglo = new ArrayList();
        for (int i = 0; i < a.size(); i++) {
            if (arreglo.contains(a.get(i))) {
            } else {
                arreglo.add(a.get(i));
            }
        }
        return arreglo;
    }


    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        ArrayList<Integer> topten = Top();
        int t = 1;
        area2.append("Palabras mas utilizadas en este documento que nos brindo \n");
        for (int i = 0; i < topten.size(); i++) {
            for (int j = 0; j < Cont.size(); j++) {
                if (Cont.get(j).equals(topten.get(i)) && t <= 10) {
                    area2.append(t + "." + "  " + acumuladas.get(j) + "   " + Cont.get(j) + "\n");
                }
            }
            t++;
        }
    }//GEN-LAST:event_jButton4MouseClicked

