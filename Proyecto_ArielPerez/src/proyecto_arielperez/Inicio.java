    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        JOptionPane.showMessageDialog(this, "Es hora de agregar un archivo que desees modificar con nosotros");
        acumuladas.clear();
        Cont.clear();
        lineas.clear();
        al.clear();
        ac.clear();
        al2.clear();
        ac2.clear();
        area.setText("");
        JFileChooser FC = new JFileChooser();
        FileNameExtensionFilter TEXTFILTER = new FileNameExtensionFilter("Archivos de Texto", "txt");
        FC.setFileFilter(TEXTFILTER);
        int action = FC.showOpenDialog(this);
        if (action == JFileChooser.APPROVE_OPTION) {
            File Archivo = null;
            FileReader fr = null;
            BufferedReader br = null;
            String linea = "";
            String parsedLine = "";
            try {
                Archivo = FC.getSelectedFile();
                fr = new FileReader(Archivo);
                br = new BufferedReader(fr);
                while ((linea = br.readLine()) != null) {
                    parsedLine = "";
                    for (int i = 0; i < linea.length(); i++) {
                        String tempo = Character.toString(linea.charAt(i));
                        if (tempo.equals(",|.|?|!|¡|¿|'|;|:|#|$|")) {

                        } else {
                            parsedLine += tempo;
                        }
                    }
                    lineas.clear();
                    String[] cambiar = parsedLine.split(" ");
                    for (int i = 0; i < cambiar.length; i++) {
                        if (cambiar[i].matches("for|if|it|the|else|what|was|from|to|in|as|and|but|or|at|why|this|on|to|a|is|with")) {

                        } else {
                            lineas.add(cambiar[i]);
                        }
                    }
                    for (int i = 0; i < lineas.size(); i++) {
                        if (acumuladas.contains(lineas.get(i))) {
                            for (int j = 0; j < acumuladas.size(); j++) {
                                if (acumuladas.get(j).equals(lineas.get(i))) {
                                    int palabra = Cont.get(j);
                                    palabra++;
                                    Cont.set(j, palabra);
                                }
                            }
                        } else {
                            acumuladas.add(lineas.get(i));
                            Cont.add(1);
                        }
                    }
                    if (lineas.size() >= 4) {
                        String d = "";
                        d = "";
                        d = lineas.get(0) + " " + lineas.get(1);
                        al.add(d);
                        ac.add(1);
                        d = "";
                        d = lineas.get(0) + " " + lineas.get(2);
                        al.add(d);
                        ac.add(1);
                        d = "";
                        d = lineas.get(0) + " " + lineas.get(3);
                        al.add(d);
                        ac.add(1);
                        d = "";
                        d = lineas.get(1) + " " + lineas.get(2);
                        al.add(d);
                        ac.add(1);
                        d = "";
                        d = lineas.get(1) + " " + lineas.get(3);
                        al.add(d);
                        ac.add(1);
                        d = "";
                        d = lineas.get(3) + " " + lineas.get(2);
                        al.add(d);
                        d = "";
                        d = lineas.get(0) + " " + lineas.get(1) + " " + lineas.get(2);
                        al2.add(d);
                        ac2.add(1);
                        d = "";
                        d = lineas.get(0) + " " + lineas.get(1) + " " + lineas.get(3);
                        al2.add(d);
                        ac2.add(1);
                        d = "";
                        d = lineas.get(3) + " " + lineas.get(2) + " " + lineas.get(1);
                        al2.add(d);
                        ac2.add(1);
                        d = "";
                        d = lineas.get(1) + " " + lineas.get(3) + " " + lineas.get(2);
                        al2.add(d);
                        ac2.add(1);
                    }
                }
                JOptionPane.showMessageDialog(this, "Aprete ahora el siguiente boton para modificarlo");
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3MouseClicked

